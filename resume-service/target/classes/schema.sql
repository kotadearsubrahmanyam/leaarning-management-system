-- Database schema for the Resume Builder Module
-- Supports PostgreSQL with JSONB columns for nested lists of structured details.

CREATE TABLE IF NOT EXISTS resumes (
    id UUID PRIMARY KEY,
    user_id VARCHAR(255) NOT NULL,
    full_name VARCHAR(255) NOT NULL,
    email VARCHAR(255) NOT NULL,
    phone VARCHAR(50),
    address TEXT,
    linkedin_url VARCHAR(255),
    github_url VARCHAR(255),
    portfolio_url VARCHAR(255),
    professional_summary TEXT,
    skills JSONB,
    education JSONB,
    experience JSONB,
    projects JSONB,
    certifications JSONB,
    achievements JSONB,
    languages JSONB,
    interests JSONB,
    template_id VARCHAR(50) DEFAULT 'ats',
    status VARCHAR(50) DEFAULT 'DRAFT',
    created_at TIMESTAMP WITHOUT TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITHOUT TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- Index on user_id to speed up lookups for a user's resumes
CREATE INDEX IF NOT EXISTS idx_resumes_user_id ON resumes(user_id);

-- Skill Master Table
CREATE TABLE IF NOT EXISTS skill_master (
    id SERIAL PRIMARY KEY,
    name VARCHAR(255) NOT NULL UNIQUE,
    category VARCHAR(255) NOT NULL,
    is_trending BOOLEAN DEFAULT FALSE,
    created_at TIMESTAMP WITHOUT TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

ALTER TABLE skill_master ADD COLUMN IF NOT EXISTS is_trending BOOLEAN DEFAULT FALSE;
ALTER TABLE skill_master ADD COLUMN IF NOT EXISTS created_at TIMESTAMP WITHOUT TIME ZONE DEFAULT CURRENT_TIMESTAMP;

